export const convert2GeoJson = (responseGql) => {
  const responseObj = [...responseGql];

  const json = [];

  responseObj.forEach((item) => {
    let geoJson = {};
    geoJson = { ...item.geom };

    const keys = Object.keys(item);
    const index = keys.indexOf('geom');
    if (index > -1) {
      keys.splice(index, 1);
    }

    keys.forEach((field) => {
      const properties = {};
      properties[field] = item[field];
      geoJson.properties = { ...geoJson.properties, ...properties };
    });

    json.push(geoJson);
  });

  const geoJsonExport = {
    type: 'FeatureCollection',
    features: json,
  };

  return geoJsonExport;
};

